package java_01;

public class TestJavaVariables {

	public static void main(String[] args) {

		boolean ouiOuNon = true;
		char alphabet_01 = 'a', alphabet_02 = 'b', alphabet_03 = 'c';
		byte bit = 127;
		short number = 32767;
		int entier = 2147483647;
		long tresLong = 2147483647;
		float flottant = 67.3F;
		double doubleFlottant = 67.3E4D;

		System.out.println("boolean = " + ouiOuNon);
		System.out.println("chars = " + alphabet_01 + ", " + alphabet_02 + ", " + alphabet_03);
		System.out.println("bit / number = " + bit + " / " + number);
		System.out.println("entier / long = " + entier + " / " + tresLong);
		System.out.println("flottant / double = " + flottant + " / " + doubleFlottant);
	}

}
